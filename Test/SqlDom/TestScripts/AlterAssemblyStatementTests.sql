-- from clause
ALTER ASSEMBLY a1 FROM 'zzz'
-- drop file
ALTER ASSEMBLY a1 DROP FILE ALL
ALTER ASSEMBLY a1 DROP FILE 'f1'
ALTER ASSEMBLY a1 DROP FILE 'f1', 'f2'

-- add file
ALTER ASSEMBLY a1 ADD FILE FROM 'p1'
ALTER ASSEMBLY a1 ADD FILE FROM 'p1', 0x10 as 'p2'

-- options
ALTER ASSEMBLY a1 WITH PERMISSION_SET = EXTERNAL_ACCESS
ALTER ASSEMBLY a1 WITH VISIBILITY = OFF, UNCHECKED DATA

-- everything combined
ALTER ASSEMBLY a1 FROM 'zzz' WITH VISIBILITY = ON DROP FILE 'f1' ADD FILE FROM 'p1'
